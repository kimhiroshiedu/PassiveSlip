function [ALAT,ALON]=XYTPL(X,Y)
%     PLTXY TRANSFORMS (X,Y) TO (ALAT,ALONG)
%  WHEN ICORD.NE.0  PLTXY MAKES NO CHANGE IN TRANSFORMATION  BETWEEN
%              (X,Y) AND (ALAT,ALONG).
global LAT0 LON0
A=6.378160e3;
E2=6.6944541e-3;
E12=6.7395719e-3;
D=5.72958e1;
RD=1.0/D;
RLATO = LAT0.*RD;
SLATO = sin(LAT0);
R     = A.*(1-E2)./sqrt((1-E2.*SLATO.^2).^3);
AN    = A./sqrt(1.0-E2.*SLATO.^2);
V2    = 1 + E12.*cos(RLATO).^2;
C1    = D./R;
C2    = D./AN;
PH1   = LAT0+C1.*Y;
RPH1  = PH1.*RD;
TPHI1 = tan(RPH1);
CPHI1 = cos(RPH1);
ALAT  = PH1-(C2.*X).^2.*V2.*TPHI1./(2.*D);
ALON  = LON0+C2.*X./CPHI1-(C2.*X).^3.*(1.0+2.*TPHI1.^2)./(6.*D.^2.*CPHI1);
